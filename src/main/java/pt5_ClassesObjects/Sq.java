package pt5_ClassesObjects;

public class Sq {
    /*
    Skeleton of Java Class
    fields - ie values x
    constructor - ie 'public Sq' and assign values
    methods - ie calcAr
     */

    double x;

    public Sq(double x) {

        this.x = x;
    }

    public double calcAr() {

        return this.x * this.x;
    }
}
